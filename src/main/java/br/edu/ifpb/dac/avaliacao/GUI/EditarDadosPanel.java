    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (!jTextFieldNome.getText().isEmpty()) {
            mainFrame.getLoggedUser().setNome(jTextFieldNome.getText());
        }
        if (!jTextFieldCpf.getText().isEmpty()) {
            mainFrame.getLoggedUser().setCpf(jTextFieldCpf.getText());
        }
        if (!jTextFieldRG.getText().isEmpty()) {
            mainFrame.getLoggedUser().setRg(jTextFieldRG.getText());
        }
        if (!jTextFieldUserName.getText().isEmpty()) {
            mainFrame.getLoggedUser().setLogin(jTextFieldUserName.getText());
        }
        if (!jPasswordFieldUser.getText().isEmpty() && !jPasswordFieldUser.getText().equals("****")) {
            mainFrame.getLoggedUser().setSenha(jPasswordFieldUser.getText());
        }
        if (!jTextFieldNascimento.getText().isEmpty()) {
            mainFrame.getLoggedUser().setDataNascimento(jTextFieldNascimento.getText());
        }
        if (!jTextFieldEmail.getText().isEmpty()) {
            mainFrame.getLoggedUser().setEmail(jTextFieldEmail.getText());
        }
        if (!jTextFieldEndereco.getText().isEmpty()) {
            mainFrame.getLoggedUser().setEndereco(jTextFieldEndereco.getText());
        }
        if (!jTextFieldBairro.getText().isEmpty()) {
            mainFrame.getLoggedUser().setBairro(jTextFieldBairro.getText());
        }
        if (!jTextFieldCidade.getText().isEmpty()) {
            mainFrame.getLoggedUser().setCidade(jTextFieldCidade.getText());
        }
        if (!jTextFieldTelefone.getText().isEmpty()) {
            mainFrame.getLoggedUser().setTelefone(jTextFieldTelefone.getText());
        }        
        if (!jPasswordFieldConta.getText().isEmpty() && !jPasswordFieldConta.getText().equals("****")) {
            mainFrame.getLoggedUser().getConta().setSenha(new String(jPasswordFieldConta.getPassword()));
        }
        
        Dao<Cliente> dao = DaoFactory.createDaoJpa();
        dao.atualizar(mainFrame.getLoggedUser());
                
    }//GEN-LAST:event_jButtonSalvarActionPerformed

