    private void jButtonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmActionPerformed
        try {
            double valor = Double.parseDouble(jTextFieldValor.getText());
            if (jButtonConfirm.getText().equals("Sacar")) {
                sacar(valor);
            } else {
                depositar(valor);
            }
        } catch (NumberFormatException e) {
            jLabeloperationResult.setForeground(Color.RED);
            jLabeloperationResult.setText("Impossivel");
        }

    }//GEN-LAST:event_jButtonConfirmActionPerformed

    private void depositar(double valor) {
        boolean resultOperation = this.mainFrame.getLoggedUser().getConta().creditar(valor);
        if (resultOperation == SUCESS) {
            jLabeloperationResult.setForeground(Color.BLUE);
            jLabeloperationResult.setText("sucesso!");
            updateAccount();
        } else {
            jLabeloperationResult.setForeground(Color.RED);
            jLabeloperationResult.setText("Quantia invalida");
        }
    }

    private void updateAccount() {
        jTextFieldValor.setText("");
        Dao<Conta> daoConta = DaoFactory.createDaoJpa();
        daoConta.atualizar(this.mainFrame.getLoggedUser().getConta());
        Dao<Cliente> daoCliente = DaoFactory.createDaoJpa();        
        this.mainFrame.getLoggedUser().setConta(daoConta.buscar(this.mainFrame.getLoggedUser().getConta().getCodigo(), Conta.class));
        jLabelSaldo.setText(String.format("%.2f",this.mainFrame.getLoggedUser().getConta().getSaldo()));
    }

    private void sacar(double valor) {
        boolean operationResult;
        if (autenticar()) {
            operationResult = this.mainFrame.getLoggedUser().getConta().debitar(valor);
            if (operationResult == SUCESS) {
                jLabeloperationResult.setForeground(Color.BLUE);
                jLabeloperationResult.setText("sucesso!");
                updateAccount();
            } else {
                jLabeloperationResult.setForeground(Color.RED);
                jLabeloperationResult.setText("Quantia invalida");
            }
        } else {
            jLabeloperationResult.setForeground(Color.RED);
            jLabeloperationResult.setText("Senha invalida!");
        }

    }

    private boolean autenticar() {
        JLabel label = new JLabel("Senha:");
        JPasswordField jpf = new JPasswordField();
        JOptionPane.showConfirmDialog(this,
                new Object[]{label, jpf}, "Autenticaçao requerida:",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        return jpf.getText().equals(mainFrame.getLoggedUser().getConta().getSenha());

    }

