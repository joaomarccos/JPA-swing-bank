    private void jButtonExcluirContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirContaActionPerformed
        int op = JOptionPane.showConfirmDialog(mainFrame, "Essa operaçao deixara voce liso! tem certeza?", "Exclusao de Conta", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(op == JOptionPane.OK_OPTION){
            Dao<Cliente> dao = DaoFactory.createDaoJpa();
            Cliente cli = dao.buscar(this.mainFrame.getLoggedUser().getLogin(), Cliente.class);
            dao.excluir(cli);
            this.mainFrame.setLoggedUser(null);
            this.mainFrame.setContentPane(new LoginPanel(mainFrame));
            this.mainFrame.setVisible(true);
        }
        
    }//GEN-LAST:event_jButtonExcluirContaActionPerformed


